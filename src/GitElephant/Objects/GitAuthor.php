namespace GitElephant\Objects;


/**
 * Represent a git author
 *
 * @author Matteo Giachino <matteog@gmail.com>
 */

class GitAuthor
{
    private $name;
    private $email;


    public function setEmail($email)
    {
        $this->email = $email;
    }

    public function getEmail()
    {
        return $this->email;
    }

    public function setName($name)
    {
        $this->name = $name;
    }

    public function getName()
    {
        return $this->name;
    }
}
