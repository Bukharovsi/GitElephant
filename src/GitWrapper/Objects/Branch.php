namespace GitWrapper\Objects;


/**
 * Branch
 *
 * An object representing a git branch
 *
 * @author Matteo Giachino <matteog@gmail.com>
 */

class Branch
{
    private $name;

    public function __construct($name)
    {
        $this->name = $name;
    }

    public function setName($name)
    {
        $this->name = $name;
    }

    public function getName()
    {
        return $this->name;
    }
}
